export default function tagColor (tag) {
    if (tag === "Open to Build Something Cool") {
        return "#fd5c63";
     } else if (tag === "Open to an Adventure") {
         return "#00BFFF";
     } else if (tag === "Open to Workout") {
         return "#32de84";
     } else if (tag === "Open to Charitable Services") {
        return "#F8C471";
    }else return "#CD7F32";
}