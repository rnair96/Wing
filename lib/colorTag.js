export default function tagColor (tag) {
    if (tag === "The Closer") {
        return "#00BFFF";
     } else if (tag === "The Entertainer") {
         return "#fd5c63";
     } else if (tag === "The Conversationalist") {
         return "#32de84";
     } else if (tag === "The Icebreaker") {
        return "#CF9FFF";
    }else return "#CD7F32";
}